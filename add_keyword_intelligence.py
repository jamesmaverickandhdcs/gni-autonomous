L("# ── LAYER 1: Unicode normalization ──────────────────────────────────────────")
L("def _normalize_text(text: str) -> str:")
L("    text = unicodedata.normalize('NFC', text)")
L("    text = ''.join(ch for ch in text if ch not in INVISIBLE_CHARS)")
L("    text = ''.join(HOMOGLYPHS.get(ch, ch) for ch in text)")
L("    text = html.unescape(text)")
L("    text = ''.join(")
L("        ch for ch in text")
L("        if (ch.isascii() and (ch.isprintable() or ch == ' '))")
L("    )")
L("    return text.strip()")
