# Main entry point
L("# ── MAIN ENTRY POINT ────────────────────────────────────────────────────────")
L("def run_keyword_sensor(articles: list) -> list:")
L("    if not articles:")
L("        return []")
L("    existing_keywords = _get_existing_keywords()")
L("    client = _get_client()")
L("    if client:")
L("        _update_watching_keywords(client)")
L("    phrase_counts = Counter()")
L("    phrase_sources = {}")
L("    phrase_contexts = {}")
L("    for art in articles:")
L("        art = _enforce_context_boundary(art)")
L("        source = art.get('source', 'Unknown')")
L("        raw_text = art.get('title', '') + ' ' + art.get('summary', '')")
L("        text = _normalize_text(raw_text)")
L("        phrases = _extract_phrases(text)")
L("        for phrase in phrases:")
L("            if phrase in existing_keywords:")
L("                continue")
L("            phrase_counts[phrase] += 1")
L("            if phrase not in phrase_sources:")
L("                phrase_sources[phrase] = set()")
L("            phrase_sources[phrase].add(source)")
L("            if phrase not in phrase_contexts:")
L("                phrase_contexts[phrase] = []")
L("            if len(phrase_contexts[phrase]) < 3:")
L("                phrase_contexts[phrase].append(text[:200])")
L("    candidates = []")
L("    for phrase, count in phrase_counts.most_common(50):")
L("        if count < MIN_ARTICLE_FREQUENCY:")
L("            break")
L("        sources = phrase_sources.get(phrase, set())")
L("        if len(sources) < MIN_SOURCE_COUNT:")
L("            continue")
L("        if count > 80:")
L("            continue")
L("        candidates.append({'keyword': phrase, 'frequency': count,")
L("                           'sources': list(sources), 'contexts': phrase_contexts.get(phrase, [])})")
L("    if not candidates:")
L("        print('  No new emerging keywords detected')")
L("        return []")
L("    print(f'  Detected {len(candidates)} candidate(s) -- running 7 layers + MAD vote')")
L("    saved = []")
L("    for cand in candidates[:10]:")
L("        keyword_raw = cand['keyword']")
L("        layer_results = {}")
L("        # Layer 1")
L("        keyword_clean = _sanitise_keyword(_normalize_text(keyword_raw))")
L("        layer_results['layer1_passed'] = bool(keyword_clean)")
L("        layer_results['layer1_reason'] = 'Normalized' if keyword_clean else 'Failed'")
L("        if not keyword_clean or not _is_safe_keyword(keyword_clean):")
L("            _log_security_event(client, keyword_raw, keyword_clean or '', cand['sources'], layer_results, 'REJECTED_LAYER1', 'INJECTION')")
L("            continue")
L("        # Layer 2")
L("        sources_set = set(cand['sources'])")
L("        trusted_set = sources_set & TRUSTED_SOURCES")
L("        layer2_ok, layer2_reason = _source_credibility_gate(sources_set, cand['frequency'])")
L("        layer_results['layer2_passed'] = layer2_ok")
L("        layer_results['layer2_reason'] = layer2_reason")
L("        if not layer2_ok:")
L("            _log_security_event(client, keyword_raw, keyword_clean, cand['sources'], layer_results, 'REJECTED_LAYER2')")
L("            print(f\"  Layer 2 rejected: '{keyword_clean}' -- {layer2_reason}\")")
L("            continue")
L("        # Layer 3 applied per-article above")
L("        layer_results['layer3_applied'] = True")
L("        # Layer 4")
L("        combined_context = ' '.join(cand['contexts'])")
L("        signature = _extract_event_signature(combined_context)")
L("        layer_results['layer4_passed'] = bool(signature)")
L("        layer_results['layer4_signature'] = signature")
L("        if not signature:")
L("            _log_security_event(client, keyword_raw, keyword_clean, cand['sources'], layer_results, 'REJECTED_LAYER4')")
L("            print(f\"  Layer 4 rejected: '{keyword_clean}' -- no event signature\")")
L("            continue")
L("        # Layer 5")
L("        groq_result = _call_groq_hardened(keyword_clean, cand['contexts'])")
L("        definition = groq_result['definition']")
L("        pillar = groq_result['pillar']")
L("        layer_results['layer5_passed'] = bool(definition)")
L("        # Layer 6")
L("        layer6_ok, layer6_reason = _final_output_gate(keyword_clean, signature)")
L("        layer_results['layer6_passed'] = layer6_ok")
L("        if not layer6_ok:")
L("            _log_security_event(client, keyword_raw, keyword_clean, cand['sources'], layer_results, 'REJECTED_LAYER6')")
L("            print(f\"  Layer 6 rejected: '{keyword_clean}' -- {layer6_reason}\")")
L("            continue")
L("        # 3-Agent MAD Vote")
L("        baseline = _get_baseline_frequency(client, keyword_clean)")
L("        layer_warnings = sum([")
L("            not layer_results.get('layer1_passed', True),")
L("            not layer_results.get('layer2_passed', True),")
L("            not layer_results.get('layer4_passed', True),")
L("            not layer_results.get('layer5_passed', True),")
L("        ])")
L("        vote_result = _run_keyword_mad_vote(")
L("            keyword=keyword_clean, signature=signature,")
L("            source_count=len(sources_set), trusted_count=len(trusted_set),")
L("            frequency=cand['frequency'], baseline_frequency=baseline,")
L("            layer_warnings=layer_warnings, definition=definition,")
L("        )")
L("        layer_results.update({")
L("            'security_veto': vote_result['security_veto'],")
L("            'agent_analyst_vote': vote_result['analyst'].get('vote', ''),")
L("            'agent_analyst_confidence': vote_result['analyst'].get('confidence', 0.0),")
L("            'agent_analyst_reason': vote_result['analyst'].get('reasoning', ''),")
L("            'agent_auditor_vote': vote_result['auditor'].get('vote', ''),")
L("            'agent_auditor_confidence': vote_result['auditor'].get('confidence', 0.0),")
L("            'agent_auditor_attack_prob': vote_result['auditor'].get('attack_probability', 0.0),")
L("            'agent_auditor_reason': vote_result['auditor'].get('reasoning', ''),")
L("            'agent_advocate_vote': vote_result['advocate'].get('vote', ''),")
L("            'agent_advocate_confidence': vote_result['advocate'].get('confidence', 0.0),")
L("            'agent_advocate_reason': vote_result['advocate'].get('reasoning', ''),")
L("            'vote_result': vote_result['vote_result'],")
L("        })")
L("        final_decision = vote_result['final_decision']")
L("        # Layer 7: audit trail")
L("        _log_security_event(client, keyword_raw, keyword_clean, cand['sources'],")
L("                            layer_results, final_decision, vote_result.get('attack_type', ''))")
L("        if final_decision == 'AUTO_REJECT':")
L("            print(f\"  AUTO-REJECT: '{keyword_clean}' -- {vote_result['vote_result']}\")")
L("            continue")
L("        if not client:")
L("            continue")
L("        try:")
L("            reemergence_row = _check_reemergence(client, keyword_clean, cand['frequency'])")
L("            if reemergence_row:")
L("                client.table('emerging_keywords').update({")
L("                    'status': 'watching',")
L("                    'frequency_count': reemergence_row['frequency_count'] + cand['frequency'],")
L("                    'source_count': len(cand['sources']),")
L("                    'last_seen': datetime.now(timezone.utc).isoformat(),")
L("                    'reemergence_count': (reemergence_row.get('reemergence_count') or 0) + 1,")
L("                    'watching_since': datetime.now(timezone.utc).isoformat(),")
L("                }).eq('id', reemergence_row['id']).execute()")
L("                msg_id = _send_telegram_keyword_alert(")
L("                    keyword_id=reemergence_row['id'], keyword=keyword_clean,")
L("                    frequency=cand['frequency'], source_count=len(cand['sources']),")
L("                    definition=definition, pillar=pillar, vote_result=vote_result, reemergence=True)")
L("                print(f\"  Re-emerged: '{keyword_clean}' ({vote_result['vote_result']})\")")
L("                saved.append(keyword_clean)")
L("                continue")
L("            existing = client.table('emerging_keywords') \\")
L("                .select('id, frequency_count, status, watching_days, watching_since') \\")
L("                .eq('keyword', keyword_clean) \\")
L("                .execute()")
L("            if existing.data:")
L("                row = existing.data[0]")
L("                client.table('emerging_keywords').update({")
L("                    'frequency_count': row['frequency_count'] + cand['frequency'],")
L("                    'last_seen': datetime.now(timezone.utc).isoformat(),")
L("                    'source_count': len(cand['sources']),")
L("                }).eq('id', row['id']).execute()")
L("                watching_days = row.get('watching_days', 0)")
L("                if row['status'] == 'watching' and watching_days > 0 and watching_days % 3 == 0:")
