L("def _get_existing_keywords() -> set:")
L("    try:")
L("        from funnel.intelligence_funnel import GEOPOLITICAL_KEYWORDS")
L("        return set(kw.lower() for kw in GEOPOLITICAL_KEYWORDS)")
L("    except Exception:")
L("        return set()")
L()
L("def _get_baseline_frequency(client, keyword: str) -> float:")
L("    if not client:")
L("        return 1.0")
L("    try:")
L("        cutoff = (datetime.now(timezone.utc) - timedelta(days=30)).isoformat()")
L("        result = client.table('keyword_security_log') \\")
L("            .select('id') \\")
L("            .eq('keyword_clean', keyword) \\")
L("            .gte('created_at', cutoff) \\")
L("            .execute()")
L("        count = len(result.data or [])")
L("        return max(float(count) / 30.0, 0.1)")
L("    except Exception:")
L("        return 1.0")
L()
L("def _extract_phrases(text: str) -> list:")
L("    text = _normalize_text(text)")
L("    text = re.sub(r'[^a-z0-9\\s-]', ' ', text.lower())")
L("    words = [w for w in text.split()")
L("             if len(w) >= MIN_WORD_LENGTH")
L("             and w not in STOP_WORDS")
L("             and _is_safe_keyword(w)]")
L("    phrases = list(words)")
L("    for i in range(len(words) - 1):")
L("        phrase = words[i] + ' ' + words[i+1]")
L("        if len(phrase) >= 8 and _is_safe_keyword(phrase):")
L("            phrases.append(phrase)")
L("    for i in range(len(words) - 2):")
L("        phrase = words[i] + ' ' + words[i+1] + ' ' + words[i+2]")
L("        if len(phrase) >= 12 and _is_safe_keyword(phrase):")
L("            phrases.append(phrase)")
L("    return phrases")
L()
L("def _check_reemergence(client, keyword: str, current_frequency: int) -> dict | None:")
L("    try:")
L("        result = client.table('emerging_keywords') \\")
L("            .select('id, status, rejected_frequency, reemergence_count, frequency_count') \\")
L("            .eq('keyword', keyword) \\")
L("            .eq('status', 'rejected') \\")
L("            .execute()")
L("        if result.data:")
L("            row = result.data[0]")
L("            rejected_freq = row.get('rejected_frequency') or row.get('frequency_count', 0)")
L("            if rejected_freq > 0 and current_frequency >= rejected_freq * REEMERGENCE_MULTIPLIER:")
L("                return row")
L("    except Exception:")
L("        pass")
L("    return None")
L()
